// Arithmatic Oprations --> +,-,*,/,%
#include<stdio.h>
int main(){
    int a,b;
    printf("Enter a: \n");
    scanf("%d",&a);
    printf("Enter b: \n");
    scanf("%d",&b);

    printf("The Sum of %d + %d =%d \n",a,b,a+b);
    printf("The Substraction of %d - %d =%d \n",a,b,a-b);
    printf("The Multiplication of %d * %d =%d \n",a,b,a*b);
    printf("The Division of %d / %d = %d\n",a,b,a/b);

}